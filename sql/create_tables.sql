DROP TABLE IF EXISTS student_performance;

CREATE TABLE student_performance (
    student_id INTEGER,
    age INTEGER,
    gender INTEGER,
    ethnicity INTEGER,
    parental_education INTEGER,
    study_time_weekly REAL,
    absences INTEGER,
    tutoring INTEGER,
    parental_support INTEGER,
    extracurricular INTEGER,
    sports INTEGER,
    music INTEGER,
    volunteering INTEGER,
    gpa REAL,
    grade_class REAL
);